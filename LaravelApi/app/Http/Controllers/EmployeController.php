        $employe->nom=$request->input('nom');
        $employe->prenom=$request->input('prenom');
        $employe->pass=$request->input('pass');
        $employe->employe_id=random_int(29,9999);
        $employe->save();
        return response()->json([
            'status' => 200,
            'message' =>'Employé ajouté !!'
        ]);
